# Faça um programa que peça ao usuário para digitar um número e informe se é par ou ímpar.

numero = int(input("Por favor, insira um valor inteiro!"))
if numero % 2 == 0:
    print("O seu número é par!")
else:
    print("O seu número é ímpar!")